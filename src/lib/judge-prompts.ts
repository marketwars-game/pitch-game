/**
 * สร้าง user message จาก stock data + pitch text
 * เนื้อหา factual ของหุ้น + pitch ของผู้เล่น (ตัวเดียวกันทั้ง 3 personas)
 */
export function buildUserMessage(stock: StockData, pitch: string): string {
  const newsLine = stock.news && stock.news.length > 0
    ? stock.news.join(' / ')
    : '(ไม่มีข่าวล่าสุด)';

  return `หุ้นที่ผู้เล่นต้อง pitch:
- ชื่อ: ${stock.name} (${stock.ticker})
- ตลาด: ${stock.exchange}
- ราคา: ${stock.price} | YTD: ${stock.ytdChange}
- Market Cap: ${stock.marketCap} | P/E: ${stock.peRatio} | Revenue Growth: ${stock.revenueGrowth}
- คำอธิบาย: ${stock.description}
- ข่าวล่าสุด: ${newsLine}

---

Pitch ของผู้เล่น (ความยาว ${pitch.length} ตัวอักษร):
"""
${pitch}
"""

ตอบเป็น JSON ตาม format ที่กำหนด`;
}

// =====================================================
// Response parser
// =====================================================

export type JudgeResponse = {
  score: number;
  comment: string;
};

/**
 * Parse JSON ที่ Claude ตอบมา
 * Strict — ถ้าไม่ valid → throw
 *
 * Defensive moves:
 * - ตัด markdown code fence ถ้าเผลอใส่มา (\`\`\`json ... \`\`\`)
 * - ตัด text นำหน้า/ตามหลัง ก่อน { และหลัง }
 * - clamp score 1-10 (เผื่อ model ตอบ 11 หรือ 0)
 * - default comment ถ้าว่าง
 */
export function parseJudgeResponse(raw: string): JudgeResponse {
  let text = raw.trim();

  // ตัด ```json ... ```
  text = text.replace(/^```(?:json)?\s*/i, '').replace(/\s*```$/i, '');

  // ตัด text นอก {...}
  const start = text.indexOf('{');
  const end = text.lastIndexOf('}');
  if (start === -1 || end === -1 || end <= start) {
    throw new Error('No JSON object found in response');
  }
  const jsonStr = text.slice(start, end + 1);

  let parsed: unknown;
  try {
    parsed = JSON.parse(jsonStr);
  } catch (err) {
    throw new Error(
      `Invalid JSON: ${err instanceof Error ? err.message : String(err)}`
    );
  }

  if (
    typeof parsed !== 'object' ||
    parsed === null ||
    !('score' in parsed) ||
    !('comment' in parsed)
  ) {
    throw new Error('Missing score or comment field');
  }

  const obj = parsed as { score: unknown; comment: unknown };

  let score: number;
  if (typeof obj.score === 'number') {
    score = obj.score;
  } else if (typeof obj.score === 'string' && !isNaN(Number(obj.score))) {
    score = Number(obj.score);
  } else {
    throw new Error('Score is not a number');
  }

  // Clamp 1-10 + round
  score = Math.max(1, Math.min(10, Math.round(score)));

  const comment = typeof obj.comment === 'string' && obj.comment.trim().length > 0
    ? obj.comment.trim()
    : '(no comment)';

  return { score, comment };
}
